   <p>
    Columnar apples are biological inovation that allows the cultivation of a great number of
    plants per unit area (1000 plants per hectar), as well as great yield on smaller areas.
    Fruit plants are grown without the firm support. All activities are done from the ground,
    as the hight of plants does not excede 3 meters. This catalogue introduces the latest
    achievements by <span style="white-space:nowrap">prof. Dr Ognjanov Vladislav</span>, in 
    the field of columnar apples selection.
   </p>
   <p>
    Columnar green apples are unique in Europe and by their quality and attractivness they
    excel Granny Smith. They always have a recognisable green colour and never get the blush.
