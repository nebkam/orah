   <p>
    Kada je ukus, miris i aroma plodova u pitanju, Leskovačka dunja nema konkurenciju.
    Nove bugarske sorte imaju vrlo krupne plodove, preko 450g, jabučastog su oblika ploda, 
    zlatno-žute boje. Plodovi su nakiselog ukusa, aromatčni, sa malo kamenastih ćelija. Posebno se
    izdvaja sorta Trijumf čija masa ploda dostiže i do 900g, a atraktivnošću, na i ovako 
    deficitarnom tržištu za sveže plodove dunje, postiže izuzetno visoku i stabilnu cenu.
   </p>
   
