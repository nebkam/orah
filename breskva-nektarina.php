    Posebnu pažnju treba obratiti izboru sorte za date uslove sredine, namenu i tržišne zahteve.
    Mogućnosti izvoza plodova breskve u zemlje zapadne i istočne Evrope je veliki. Dinamične 
    promene sortimenta breskve u svetu prati i sortiment koji je u procesu stavljanja na sortnu 
