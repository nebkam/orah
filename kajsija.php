   <p>
    Plodovi kajsije su cenjeno sveže voće ili se preradjuju i prodaju kao sušene, dok se u Srbiji 
    najviše koriste za proizvodnju sokova, džemova, kompota i vrhunske rakije. Sem za potrebe 
    ishrane, kajsija se koristi kao dekorativna vrsta, a njeno drvo je visokih mehaničkih i 
    dekorativnih kvaliteta. Sem ploda, i seme kajsije se koristi u prehrambenoj industriji, 
    najčešće kao zamena za seme badema. Od ovog semena se spravlja italijanski liker Amareto, 
    kao i amareti biskviti. Ulje iz semena kajsije se ponegde upotrebljava kao jestivo ulje.
