   <p>
    Dogwood ( <i>Cornus mas</i> ) is a type which is rich in biologically valuable fruits with
    multiple applications. It could be found in Serbian forests and is resistant to 
    disease-causing agents. Long ago this feature was observed by Serbian people and good 
    health compared with a saying: &quot;healthy as a dogwood&quot; meaning &quot;fit as a
    fiddle&quot;. Our king-sized selections are pleasant to eat at full maturity. Dogwood has 
    a unique nutritional value due to the high content of vitamins, minerals and protective
    substances. It is suitable for various forms of processing because it gives jams, juices,
    brandy, and all this of the highest quality.
   </p>
   <p>
    Our desire is to introduce this sort into plantation cultivation and that no garden or yard
    are without these unique valuable fruits.
   </p>
   <p>
    All trees have large fruit, more than 8gr with elongated cylindrical shape, deep red in
    color and sweet sour in taste.
